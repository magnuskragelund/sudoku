// Helper function to create a board from a flat array
export function createBoard(flatArray) {
  const board = [];
  for (let i = 0; i < 9; i++) {
    board.push(flatArray.slice(i * 9, (i + 1) * 9));
  }
  return board;
}

// Helper function to create a deep copy of a board
export function copyBoard(board) {
  return board.map(row => [...row]);
}

